# ==================== 参数解析 ====================
parse_args() {
    SKIP_PIP_NPM=false
    SKIP_GIT=false
    SKIP_IMSG=false
    
    for arg in "$@"; do
        case $arg in
            --skip-pip-npm) SKIP_PIP_NPM=true ;;
            --skip-git) SKIP_GIT=true ;;
            --skip-imessage) SKIP_IMSG=true ;;
            --check-only)
                final_check
                exit 0
                ;;
            --help|-h)
                show_help
                exit 0
                ;;
        esac
    done
}

# 启动主流程
main "$@"
