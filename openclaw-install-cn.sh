# ==================== 主流程 ====================
main() {
    echo ""
    echo "╔═══════════════════════════════════════════════════════════╗"
    echo "║        OpenClaw 中国版标准安装包 v1.0.0                   ║"
    echo "║              专为大陆网络环境优化                         ║"
    echo "╚═══════════════════════════════════════════════════════════╝"
    echo ""
    
    parse_args "$@"
    
    detect_system
    config_homebrew_cn
    config_pip_npm
    config_git
    install_dependencies
    install_openclaw
    install_skills
    
    if [[ "$SKIP_IMSG" != "true" ]]; then
        setup_imessage
    fi
    
    init_workspace
    generate_sample_config
    final_check
    
