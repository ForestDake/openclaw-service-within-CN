# ==================== 最终检查 ====================
final_check() {
    log_info "最终检查..."
    
    CHECKS=(
        "brew --version:Homebrew"
        "node --version:Node.js"
        "python3 --version:Python"
        "openclaw --version:OpenClaw CLI"
        "imsg --version:iMessage CLI"
    )
    
    echo ""
    echo "═══════════════════════════════════════════════════════════"
    echo "                     安装检查结果                          "
    echo "═══════════════════════════════════════════════════════════"
    
    for check in "${CHECKS[@]}"; do
        CMD="${check%%:*}"
        NAME="${check##*:}"
        
        if $CMD &>/dev/null; then
            VERSION=$($CMD 2>/dev/null | head -1)
            echo "✅ $NAME: $VERSION"
        else
            echo "❌ $NAME: 未安装"
        fi
    done
    
    echo ""
    echo "═══════════════════════════════════════════════════════════"
}

