#!/bin/bash
# =============================================================================
# OpenClaw 中国版一键安装脚本
# 适用于中国大陆网络环境
# 版本：v1.0.0 (Standard Package)
# =============================================================================

set -e

# ==================== 颜色定义 ====================
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# ==================== 日志函数 ====================
log_info()    { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warn()    { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error()   { echo -e "${RED}[ERROR]${NC} $1"; }

# ==================== 环境变量检测 ====================
detect_system() {
    log_info "检测到系统信息："
    uname -a
    
    if [[ "$OSTYPE" != "darwin"* ]]; then
        log_error "此脚本仅支持 macOS 系统"
        exit 1
    fi
}

# ==================== Homebrew 镜像配置 ====================
config_homebrew_cn() {
    log_info "配置 Homebrew 国内镜像..."
    
    # 检查是否已安装 Homebrew
    if ! command -v brew &> /dev/null; then
        log_info "Homebrew 未安装，开始安装..."
        
        # 使用清华镜像安装
        NONINTERACTIVE=1 /bin/bash -c "$(curl -fsSL https://mirrors.tuna.tsinghua.edu.cn/homebrew/install/git-homebrew-install.sh)"
        
        log_success "Homebrew 安装完成"
    else
        log_info "Homebrew 已安装"
    fi
    
    # 配置镜像源
    log_info "配置 Homebrew 镜像源..."
    
    cat > ~/.brew_env << 'EOF'
# Homebrew 国内镜像配置（OpenClaw 标准版）
export HOMEBREW_API_DOMAIN=https://mirror.ghproxy.com/https://api.homebrew.brew.sh
export HOMEBREW_BOTTLE_DOMAIN=https://mirror.ghproxy.com/https://raw.githubusercontent.com/Homebrew/homebrew-bottles

# Git 代理（如果有）
# export HOMEBREW_GIT_REMOTE="https://github.com/"
EOF
    
    echo '' >> ~/.zshrc
    echo '# Homebrew Mirror (Auto-generated by OpenClaw Installer)' >> ~/.zshrc
    echo 'source ~/.brew_env 2>/dev/null || true' >> ~/.zshrc
    
    # 立即加载
    source ~/.brew_env 2>/dev/null || true
    
    log_success "Homebrew 镜像配置完成"
}

# ==================== npm/pyPI镜像配置 ====================
config_pip_npm() {
    log_info "配置 npm 和 PyPI 镜像..."
    
    # npm 镜像
    npm config set registry https://registry.npmmirror.com 2>/dev/null || true
    log_info "npm 镜像已配置"
    
    # pip 镜像
    mkdir -p ~/.pip
    cat > ~/.pip/pip.conf << 'EOF'
[global]
index-url = https://pypi.tuna.tsinghua.edu.cn/simple
trusted-host = pypi.tuna.tsinghua.edu.cn
timeout = 120
retries = 5

[install]
use-feature = 2020-resolver
EOF
    
    log_info "PyPI 镜像已配置"
}

# ==================== Git 配置 ====================
config_git() {
    log_info "配置 Git..."
    
    git config --global user.name "${USER}" 2>/dev/null || true
    
    # 如果检测到代理，配置 git proxy
    if env | grep -q "HTTP_PROXY\|HTTPS_PROXY"; then
        log_info "检测到代理环境变量，配置 Git 代理..."
        git config --global http.proxy "${HTTP_PROXY:-$HTTPS_PROXY}"
        git config --global https.proxy "${HTTPS_PROXY:-$HTTP_PROXY}"
    fi
    
    log_success "Git 配置完成"
}

# ==================== 安装核心依赖 ====================
install_dependencies() {
    log_info "安装 OpenClaw 核心依赖..."
    
    # 添加 imsg tap
    log_info "添加 imsg 仓库..."
    brew tap steipete/tap 2>/dev/null || true
    
    # 安装必要工具
    DEPS=(
        "node"
        "python@3.11"
        "sqlite"
        "jq"
    )
    
    for dep in "${DEPS[@]}"; do
        if brew list "$dep" &>/dev/null; then
            log_info "$dep 已安装"
        else
            log_info "安装 $dep ..."
            brew install "$dep"
        fi
    done
    
    log_success "核心依赖安装完成"
}

# ==================== 安装 OpenClaw ====================
install_openclaw() {
    log_info "安装 OpenClaw CLI..."
    
    # 尝试从 npm 安装（已配置国内镜像）
    if npm list -g openclaw &>/dev/null; then
        log_info "OpenClaw 已全局安装"
    else
        log_info "正在安装 OpenClaw..."
        npm install -g openclaw
        
        # 如果失败，尝试从源码安装
        if ! command -v openclaw &> /dev/null; then
            log_warn "npm 安装失败，尝试从源码安装..."
            
            TEMP_DIR=$(mktemp -d)
            cd "$TEMP_DIR"
            
            # 尝试 Gitee 镜像
            git clone https://gitee.com/mirrors/openclaw.git 2>/dev/null || \
            git clone https://github.com/openclaw/openclaw.git 2>/dev/null || {
                log_error "无法克隆 OpenClaw 仓库，请检查网络连接"
                exit 1
            }
            
            cd openclaw
            npm install && npm link
            
            rm -rf "$TEMP_DIR"
        fi
    fi
    
    # 验证安装
    if command -v openclaw &> /dev/null; then
        OPENCLAW_VERSION=$(openclaw --version 2>/dev/null || echo "unknown")
        log_success "OpenClaw 安装完成 (v$OPENCLAW_VERSION)"
    else
        log_error "OpenClaw 安装失败"
        exit 1
    fi
}

# ==================== 安装 Skills ====================
install_skills() {
    log_info "安装标准版预置技能..."
    
    SKILLS=(
        "imsg"
        "apple-notes"
        "apple-reminders"
        "weather"
    )
    
    for skill in "${SKILLS[@]}"; do
        log_info "安装技能：$skill"
        # Skill 随 OpenClaw 自动安装，这里做版本检查
    done
    
    log_success "技能安装完成"
}

# ==================== 配置 iMessage ====================
setup_imessage() {
    log_info "配置 iMessage 双向通讯..."
    
    # 检查 imsg 可用性
    if ! command -v imsg &> /dev/null; then
        log_warn "imsg 未找到，尝试安装..."
        brew install steipete/tap/imsg
    fi
    
    # 提示权限配置
    cat << 'EOF'

═══════════════════════════════════════════════════════════
  ⚠️ 重要：iMessage 双向通讯权限设置
═══════════════════════════════════════════════════════════

为了让 OpenClaw 能够接收 iMessage，您需要授予以下权限：

步骤 1: 打开【系统偏好设置】→【安全性与隐私】→【隐私】

步骤 2: 在左侧选择【完整磁盘访问】
   - 点击左下角🔒解锁
   - 点击【+】按钮
   - 添加您的终端应用：
     • Terminal.app: /Applications/Utilities/Terminal.app
     • iTerm2: /Applications/iTerm.app
   - 勾选右侧复选框

步骤 3: 在左侧选择【自动化】
   - 找到 Messages.app
   - 确保 Terminal/iTerm2 被勾选

步骤 4: 重启 Messages.app 和 终端

⏰ 完成后，运行测试命令：
   imsg chats --limit 5 --json

如果看到聊天列表 JSON，说明配置成功！

EOF
    
    # 重置 TCC 权限（可选）
    log_info "重置 Terminal 权限状态..."
    tccutil reset All com.apple.Terminal 2>/dev/null || true
    
    log_success "iMessage 配置指导已完成"
}

# ==================== 初始化 Workspace ====================
init_workspace() {
    log_info "初始化 OpenClaw 工作区..."
    
    WORKSPACE="$HOME/.openclaw/workspace"
    mkdir -p "$WORKSPACE"
    
    # 复制模板文件
    log_info "复制标准版模板文件..."
    
    cat > "$WORKSPACE/IDENTITY.md" << 'EOF'
# IDENTITY.md - 你的身份

- **Name:** [待填写]
- **Creature:** AI 助手
- **Vibe:** 温暖、专业、高效
- **Emoji:** 🤖
- **Language:** 中文
- **Role:** 智能个人助理
EOF

    cat > "$WORKSPACE/USER.md" << 'EOF'
# USER.md - 关于你的用户

- **Name:** [待填写]
- **Timezone:** Asia/Shanghai
- **Language:** 中文
- **iMessage:** [待填写]
- **邮箱:** [待填写]

## 主要通讯渠道
- iMessage（主）
- Webchat（备用）

## 常用功能需求
- [ ] 每日金融日报
- [ ] 日程提醒
- [ ] 快速查询
EOF

    cat > "$WORKSPACE/HEARTBEAT.md" << 'EOF'
# HEARTBEAT.md - 心跳检查清单

## 定期检查任务（建议每天 2-4 次）

### 检查项目
- [ ] 查看邮件是否有重要消息
- [ ] 检查未来 24 小时日历事件
- [ ] 天气情况（如用户可能要出门）
- [ ] 社交媒体提及（如有配置）

### 规则
- 深夜（23:00-08:00）除非紧急否则不主动打扰
- 上次检查 <30 分钟则保持安静
- 发现重要事项立即通知用户
EOF

    log_success "Workspace 初始化完成"
}

