╚═══════════════════════════════════════════════════════════╝

用法: ./openclaw-install-cn.sh [选项]

选项:
  --skip-pip-npm     跳过 pip/npm 镜像配置
  --skip-git         跳过 Git 配置  
  --skip-imessage    跳过 iMessage 配置
